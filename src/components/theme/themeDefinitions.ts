export const THEMES: ThemeDefinition[] = THEME_DRAFTS
  .map(completeTheme)
  .sort((a, b) => {
    const orderA = THEME_ORDER_INDEX.get(a.id) ?? Number.MAX_SAFE_INTEGER;
    const orderB = THEME_ORDER_INDEX.get(b.id) ?? Number.MAX_SAFE_INTEGER;
    return orderA - orderB;
  });

