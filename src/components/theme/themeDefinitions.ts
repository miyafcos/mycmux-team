import type { ThemeColorScheme, ThemeDefinition, ThemeGroup, ThemeStatusColors } from "../../types";

export const DEFAULT_THEME_ID = "mayonaka";

export const THEME_GROUPS: Array<{ id: ThemeGroup; label: string; hint: string }> = [
  { id: "calm-dark", label: "静かな暗色", hint: "長時間作業向け" },
  { id: "vivid-dark", label: "個性の強い暗色", hint: "色の違いが明確" },
  { id: "light", label: "明るい配色", hint: "昼間や資料作業向け" },
];

type ThemeDraft = Omit<ThemeDefinition, "colorScheme" | "chrome" | "status" | "notification"> & {
  colorScheme?: ThemeColorScheme;
  chrome: Pick<ThemeDefinition["chrome"], "background" | "surface" | "border" | "text" | "textMuted" | "accent"> &
    Partial<Pick<ThemeDefinition["chrome"], "textDim" | "hover" | "selected" | "danger">>;
  status?: Partial<ThemeStatusColors>;
  notification?: string;
};

const DEFAULT_DARK_STATUS: ThemeStatusColors = {
  working: "#60a5fa",
  waiting: "#f6c177",
  done: "#4ade80",
  error: "#fb7185",
};

const DEFAULT_LIGHT_STATUS: ThemeStatusColors = {
  working: "#2f6fed",
  waiting: "#b9781f",
  done: "#2f9a68",
  error: "#c85757",
};

function completeTheme(theme: ThemeDraft): ThemeDefinition {
  const colorScheme: ThemeColorScheme = theme.colorScheme ?? (theme.group === "light" ? "light" : "dark");
  const isLight = colorScheme === "light";
  const statusBase = isLight ? DEFAULT_LIGHT_STATUS : DEFAULT_DARK_STATUS;

  return {
    ...theme,
    colorScheme,
    chrome: {
      ...theme.chrome,
      textDim: theme.chrome.textDim ?? (isLight ? "#9b938d" : "#475569"),
      hover: theme.chrome.hover ?? (isLight ? "rgba(0, 0, 0, 0.055)" : "rgba(255, 255, 255, 0.065)"),
      selected: theme.chrome.selected ?? (isLight ? "rgba(0, 0, 0, 0.082)" : "rgba(255, 255, 255, 0.085)"),
      danger: theme.chrome.danger ?? (isLight ? "#c85757" : "#fb7185"),
    },
    status: {
      ...statusBase,
      ...theme.status,
    },
    notification: theme.notification ?? theme.status?.waiting ?? statusBase.waiting,
  };
}

